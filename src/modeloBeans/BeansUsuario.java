package modeloBeans;

/**
 *
 * @author MarceloMartinsVilara
 */
public class BeansUsuario {
    private int cod_usu;
    private String nome_usu;
    private String senha_usu;
    private String tipo_usu;
    private String pesquisa_usu;

    /**
     * @return the cod_usu
     */
    public int getCod_usu() {
        return cod_usu;
    }

    /**
     * @param cod_usu the cod_usu to set
     */
    public void setCod_usu(int cod_usu) {
        this.cod_usu = cod_usu;
    }

    /**
     * @return the nome_usu
     */
    public String getNome_usu() {
        return nome_usu;
    }

    /**
     * @param nome_usu the nome_usu to set
     */
    public void setNome_usu(String nome_usu) {
        this.nome_usu = nome_usu;
    }

    /**
     * @return the senha_usu
     */
    public String getSenha_usu() {
        return senha_usu;
    }

    /**
     * @param senha_usu the senha_usu to set
     */
    public void setSenha_usu(String senha_usu) {
        this.senha_usu = senha_usu;
    }

    /**
     * @return the tipo_usu
     */
    public String getTipo_usu() {
        return tipo_usu;
    }

    /**
     * @param tipo_usu the tipo_usu to set
     */
    public void setTipo_usu(String tipo_usu) {
        this.tipo_usu = tipo_usu;
    }

    /**
     * @return the pesquisa_usu
     */
    public String getPesquisa_usu() {
        return pesquisa_usu;
    }

    /**
     * @param pesquisa_usu the pesquisa_usu to set
     */
    public void setPesquisa_usu(String pesquisa_usu) {
        this.pesquisa_usu = pesquisa_usu;
    }
}
